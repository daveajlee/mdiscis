package de.davelee.mdiscis.config;

public class MenuConfig {
	
	private String fileText;
	private String newText;
	private String loadText;
	private String saveText;
	private String exitText;
	private String helpText;
	private String contentsText;
	private String aboutText;
	
	public String getFileText() {
		return fileText;
	}
	
	public void setFileText(final String fileText) {
		this.fileText = fileText;
	}
	
	public String getNewText() {
		return newText;
	}
	
	public void setNewText(final String newText) {
		this.newText = newText;
	}
	
	public String getLoadText() {
		return loadText;
	}
	
	public void setLoadText(final String loadText) {
		this.loadText = loadText;
	}
	
	public String getSaveText() {
		return saveText;
	}
	
	public void setSaveText(final String saveText) {
		this.saveText = saveText;
	}
	
	public String getExitText() {
		return exitText;
	}
	
	public void setExitText(final String exitText) {
		this.exitText = exitText;
	}
	
	public String getHelpText() {
		return helpText;
	}
	
	public void setHelpText(final String helpText) {
		this.helpText = helpText;
	}
	
	public String getContentsText() {
		return contentsText;
	}
	
	public void setContentsText(final String contentsText) {
		this.contentsText = contentsText;
	}
	
	public String getAboutText() {
		return aboutText;
	}
	
	public void setAboutText(final String aboutText) {
		this.aboutText = aboutText;
	}

}
